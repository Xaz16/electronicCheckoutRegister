CREATE TABLE organizations (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    inn TEXT NOT NULL UNIQUE,
    boss_id INTEGER
);

CREATE TABLE locations (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    country TEXT NOT NULL,
    city TEXT NOT NULL,
    street TEXT NOT NULL,
    build TEXT NOT NULL,
    organization_id INTEGER NOT NULL,
    FOREIGN KEY (organization_id) REFERENCES organizations(id)
);



CREATE TABLE employees (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    position TEXT NOT NULL,
    organization_id INTEGER NOT NULL,
    FOREIGN KEY (organization_id) REFERENCES organizations(id)
);

CREATE TABLE workshifts (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    start_time TEXT NOT NULL,
    employee_id INTEGER NOT NULL,
    FOREIGN KEY (employee_id) REFERENCES employees(id)
);

CREATE TABLE checkout_registers (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    location_id INTEGER NOT NULL,
    organization_id INTEGER NOT NULL,
    FOREIGN KEY (location_id) REFERENCES locations(id),
    FOREIGN KEY (organization_id) REFERENCES organizations(id)
);

CREATE TABLE devices (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    type TEXT NOT NULL,
    register_id INTEGER NOT NULL,
    FOREIGN KEY (register_id) REFERENCES checkout_registers(id)
);

CREATE TABLE products (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    category TEXT NOT NULL,
    price REAL NOT NULL
);

CREATE TABLE scan_codes (
    number TEXT PRIMARY KEY,
    product_id INTEGER NOT NULL,
    FOREIGN KEY (product_id) REFERENCES products(id)
);

CREATE TABLE customers (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL
);

CREATE TABLE discount_cards (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    type TEXT NOT NULL,
    customer_id INTEGER NOT NULL,
    FOREIGN KEY (customer_id) REFERENCES customers(id)
);

CREATE TABLE bank_cards (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    number TEXT NOT NULL UNIQUE,
    cvv TEXT NOT NULL,
    expire_date TEXT NOT NULL,
    customer_id INTEGER NOT NULL,
    FOREIGN KEY (customer_id) REFERENCES customers(id)
);

CREATE TABLE buy_lists (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    totalSum INTEGER NOT NULL
);

CREATE TABLE buy_list_items (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    quantity INTEGER NOT NULL,
    bought_price INTEGER NOT NULL,
    buy_lists INTEGER NOT NULL,
    product_id INTEGER NOT NULL,
    FOREIGN KEY (buy_lists) REFERENCES buy_lists(id),
    FOREIGN KEY (product_id) REFERENCES products(id)
);

CREATE TABLE receipts (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    time TEXT NOT NULL,
    workshift_id INTEGER NOT NULL,
    customer_id INTEGER NOT NULL,
    bank_card_id INTEGER NOT NULL,
    checkout_register_id INTEGER NOT NULL,
    buy_list_id INTEGER NOT NULL,
    FOREIGN KEY (workshift_id) REFERENCES workshifts(id),
    FOREIGN KEY (customer_id) REFERENCES customers(id),
    FOREIGN KEY (bank_card_id) REFERENCES bank_cards(id),
    FOREIGN KEY (checkout_register_id) REFERENCES checkout_registers(id),
    FOREIGN KEY (buy_list_id) REFERENCES buy_lists(id)
);

